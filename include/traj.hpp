bool is_close_enough(double* current, double* goal){
    double total_distance = 0;
    for(int i = 0; i < 6; i++){
        total_distance += pow(current[i] - goal[i], 2);
    }
    total_distance = sqrt(total_distance);
    if(total_distance > TrajectoryVars.GOAL_TOLERANCE){
        return false;
    }

    return true;
}

